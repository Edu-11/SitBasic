                      el transporte de dos unidades a cargo de una sola persona. <br>
                      En el año 1895 la Reina Regente María Cristina autorizó la construcción de cien bancos para
                      el servicio de cuarteles y guardias del Ejército Español. Algunos de ellos han llegado en
                      buen estado hasta nuestros días debido a la durabilidad de sus materiales y simplicidad de
                      su diseño.
                  </p>
