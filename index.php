                  <h2>Rediseñado para el futuro</h2>
                  <p> En 2010 el fotógrafo Javier Sardá restauró un banco plegable, que ya había utilizado durante
                      su
                      servicio militar obligatorio, adquirido en los años 90 debido al cierre del Cuartel de
                      Caballería de Barcelona. <br>
                      Durante el proceso descubre las posibilidades de su estructura plegable y la moderniza,
                      diseña su estructura con materiales de ferretería estándar que permite diferentes alturas y
                      diferentes anchuras.
                      Además de reforzar su durabilidad mediante la fabricación de sus piezas en acero plegado sin
                      soldaduras. <br>
                      De esta manera crea una pieza de mobiliario atemporal, que se adapta a las tendencias y que,
                      en una época de obsolescencia programada, aporta fiabilidad y durabilidad por muchos años.
